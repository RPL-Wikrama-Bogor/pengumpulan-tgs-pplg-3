<!-- untuk membuat.. -->
<template>
  <!-- templating -->
  <p>----------template----------</p>
  <p>
    {{ nama }}
    {{ number }}
  <div v-html="kelas"></div>
  </p>
  <p>----------data binding----------</p>
  <!-- blading -->
  <button :disabled="show">button</button>
  <h1 v-bind="property">gacor kang</h1>
  <input :type="typeInput">
  <p>----------data binding----------</p>
  {{ count == 0 ? count + 1 : count + 2 }}
  <p>-----------v-if----------</p>
  <button v-if="show">button</button>
  <button v-show="show">PERIGUD PERI WELL</button>

  <div v-if="count == 1">
    number 1
  </div>

  <div v-else-if="count == 2">
    number 2
  </div>

  <div v-else="count == 3">
    number 3
  </div>
  <p>----------computed dan method----------</p>
<!-- <button @click="counterNumber">methods {{ counterButton }}</button>
      <button @click="countComputed">computed {{ numberComputed }}</button> -->
  <input :type="typeInput">
  <button @click="showPassword">Show Password</button>

  <p>----------class dan style binding----------</p>
  <ul>
    <li class="fs" :class="{ active: isActive, fs40px: isActive }">
      KANGJUT <p class="ada" :class="{ adaDa: isActive}" >BADAG</p>
    </li>
  </ul>
  <button @click="ubahWarna">ubah warna</button>

  <p>----------list dan rendering----------</p>

    <ul>
      <li v-for="(item,index) in daftarKelas">
        {{ index+1 }}
        {{ item }}</li>
    </ul>

  <p>----------v-model ----------</p>
  <input type="text" v-model="inputKelas">
  {{ inputKelas }}
</template>

<script>
export default {
  data() {
    return {
      inputKelas: '',
      daftarKelas:['RPL 1', 'RPL 2', 'RPL 3'],
      counterButton: 0,
      numberComputed: 0,
      show: false,
      count: 3,
      nama: 'gama',
      number: 1,
      kelas: '<h1>kelas 11</h1>',
      nonaktif: true,
      property: {
        id: 1,
        class: 'color'
      },
      typeInput: "password",
      isActive: false,
    }
  },
  methods: {
    counterNumber() {
      this.counterButton += 1;
    },
    showPassword() {
      if (this.typeInput == 'password') {
        this.typeInput = 'text';
      } else {
        this.typeInput = 'password';
      }
    },
    ubahWarna() {
      if (this.isActive) {
        this.isActive = false;
      } else {
        this.isActive = true;
      }
    },

  },
  computed: {
    countComputed() {
      this.numberComputed += 6;
    }
  },


}
</script>

